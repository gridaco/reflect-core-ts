/**
 * - [flutter](https://api.flutter.dev/flutter/painting/BorderStyle.html)
 * - [css](https://developer.mozilla.org/en-US/docs/Web/CSS/border-style)
 */
export enum BorderStyle {
    /**
     * no border
     */
    none = "none",
    /**
     * border is hidden (supported only by css)
     */
    hidden = "hidden",
    /**
     * border is present, solid
     */
    solid = "solid",
    /**
     * border is dotted with cicle dots (supported only by css)
     */
    dotted = "dotted",
    /**
     * border is dashed (supported only by css)
     */
    dashed = "dashed",
    /**
     * border is doubled (supported only by css)
     */
    double = "double",
}
